        let currentSlideIndex = 0;
        const slides = document.querySelectorAll('.slide');
        const indicators = document.querySelectorAll('.indicator-bar');
        const totalSlides = slides.length;
        let slideInterval;

        function updateSliderVisibility() {
            slides.forEach((slide, index) => {
                if (index === currentSlideIndex) {
                    slide.classList.remove('opacity-0', 'z-0');
                    slide.classList.add('opacity-100', 'z-10');
                    // Retrigger subtle scaling animation on active background image
                    const bgImage = slide.querySelector('.bg-cover');
                    if (bgImage) {
                        bgImage.classList.remove('scale-100');
                        bgImage.classList.add('scale-105');
                    }
                } else {
                    slide.classList.remove('opacity-100', 'z-10');
                    slide.classList.add('opacity-0', 'z-0');
                    const bgImage = slide.querySelector('.bg-cover');
                    if (bgImage) {
                        bgImage.classList.remove('scale-105');
                        bgImage.classList.add('scale-100');
                    }
                }
            });

            // Update interactive linear indicators
            indicators.forEach((indicator, index) => {
                if (index === currentSlideIndex) {
                    indicator.classList.remove('bg-white/30');
                    indicator.classList.add('bg-orange-500');
                } else {
                    indicator.classList.remove('bg-orange-500');
                    indicator.classList.add('bg-white/30');
                }
            });
        }

        function nextSlide() {
            currentSlideIndex = (currentSlideIndex + 1) % totalSlides;
            updateSliderVisibility();
        }

        function prevSlide() {
            currentSlideIndex = (currentSlideIndex - 1 + totalSlides) % totalSlides;
            updateSliderVisibility();
        }

        function goToSlide(index) {
            currentSlideIndex = index;
            updateSliderVisibility();
            resetAutoplayTimer();
        }

        function startAutoplayTimer() {
            slideInterval = setInterval(nextSlide, 6000); // Fluid slide rotation every 6 seconds
        }

        function resetAutoplayTimer() {
            clearInterval(slideInterval);
            startAutoplayTimer();
        }

        // Attach DOM Events
        document.getElementById('next-btn').addEventListener('click', () => {
            nextSlide();
            resetAutoplayTimer();
        });

        document.getElementById('prev-btn').addEventListener('click', () => {
            prevSlide();
            resetAutoplayTimer();
        });

        // Initialize Slider
        startAutoplayTimer();
    </script>

